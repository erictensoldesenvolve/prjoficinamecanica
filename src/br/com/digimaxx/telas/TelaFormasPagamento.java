    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtFid.getText().equals("")){
            if(txtFnome.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Digite o nome da Forma de Pagamento!");
            }else{
                final TelaProgresso carregando = new TelaProgresso();
                        carregando.setVisible(true);
                        Thread t = new Thread(){
                            public void run(){
                                //Gera o arquivo pdf
                                try {
                                    sleep(2000);
                            if(formaPagamento.inserir(txtFnome.getText().toUpperCase())){
                                txtFnome.setText(null);
                                tabelaVazia();
                            }
                            carregando.dispose();
                        } catch (Exception e) {
                           JOptionPane.showMessageDialog(null, "Erro: "+e);
                        }
                            }
                        };
                        t.start();   
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

