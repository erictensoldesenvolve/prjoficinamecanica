    //LISTA AS FORMAS DE PAGAMENTO
    private void tabelaVazia(){                
        ArrayList dados = new ArrayList();
        String[] colunas = new String[]{"Código", "Nome"};
        
        ArrayList<FormasPagamento> lista = formaPagamento.listar();
            for(FormasPagamento i:lista){
               dados.add(new Object[]{
                        i.getfId(),
                        i.getfNome()                          
                      }); 
            }
       
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        //seta o modelo da tabela pela classe modelo
        tblFormaPag.setModel(modelo);
        
        //Decide se pode ou não reordenar a tabela
        tblFormaPag.getTableHeader().setReorderingAllowed(true);
        // Cabeçalho com altura de 50px
        tblFormaPag.getTableHeader().setPreferredSize(new Dimension(0, 40));
        
        tblFormaPag.setAutoResizeMode(tblFormaPag.AUTO_RESIZE_ALL_COLUMNS);
        tblFormaPag.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        //ALTERA A COR DO CABEÇALHO
        Color cor = new Color(102, 102, 102);
        DefaultTableCellRenderer cabecalho = new DefaultTableCellRenderer();
        cabecalho.setBackground(cor);
        cabecalho.setForeground(Color.white);

        for (int i = 0; i < tblFormaPag.getModel().getColumnCount(); i++) {
               tblFormaPag.getColumnModel().getColumn(i).setHeaderRenderer(cabecalho);
        } 
      
            //ALTERA A COR DA BORDA DE FORA DO JSCROLLPANE
            jScrollPane1.setBorder(BorderFactory.createLineBorder(cor));
            jScrollPane1.getViewport().setBackground(Color.white);
            tblFormaPag.setBackground(Color.white);
            tblFormaPag.setForeground(Color.black);
            //altera tamanho das colunas da tabela
            tblFormaPag.getColumnModel().getColumn(0).setPreferredWidth(20);
            tblFormaPag.getColumnModel().getColumn(0).setResizable(false);
            tblFormaPag.getColumnModel().getColumn(1).setPreferredWidth(200);
            tblFormaPag.getColumnModel().getColumn(1).setResizable(false);
            
    }
    
    //SELECIONA O ITEM DA TABELA PARA EXCLUSÃO
    private void setar(){
        int setar = tblFormaPag.getSelectedRow();
        txtFid.setText(tblFormaPag.getModel().getValueAt(setar, 0).toString());  
        txtFid.requestFocus();
    }

