    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(txtCatNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Digite um nome para categoria.");
        }else if(!txtCatId.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Categoria já cadastrada no banco de dados,\n clique em editar para salvar alterações.");
        }else{
            final TelaProgresso carregando = new TelaProgresso();
            carregando.setVisible(true);
            Thread t = new Thread(){
                public void run(){
                    //Gera o arquivo pdf
                    try {
                        sleep(2000);
                        categoria.cadastrar(txtCatNome.getText().toUpperCase());
                        limparCampos();
                        tabelaVazia();
                        carregando.dispose();
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, "Erro: "+e);
                    }
                }
            };
            t.start();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

