    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if(txtCatId.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Clique em um item da tabela para alterar.");
        }else if(txtCatNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo nome não pode ser vazio.");
        }else{
            final TelaProgresso carregando = new TelaProgresso();
                carregando.setVisible(true);
                Thread t = new Thread(){
                    public void run(){
                        //Gera o arquivo pdf
                        try {
                            sleep(2000);
                            categoria.editar(txtCatNome.getText(), Integer.parseInt(txtCatId.getText()));
                            tabelaVazia();
                            limparCampos();
                            carregando.dispose();
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Erro: "+e);
                        }
                    }
                };
                t.start();
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if(txtCatId.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Selecione na tabela a categoria para excluir.");
        }else{
            int confirma = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir a categoria "+txtCatId.getText() + "?", "Atenção", JOptionPane.YES_NO_OPTION);
                if (confirma == JOptionPane.YES_OPTION) {
                    final TelaProgresso carregando = new TelaProgresso();
                    carregando.setVisible(true);
                    Thread t = new Thread(){
                        public void run(){
                            //Gera o arquivo pdf
                            try {
                                sleep(2000);
                                categoria.excluir(Integer.parseInt(txtCatId.getText()));
                                tabelaVazia();
                                limparCampos();
                                carregando.dispose();
                            } catch (Exception e) {
                                JOptionPane.showMessageDialog(null, "Erro: "+e);
                            }
                        }
                    };
                    t.start();
                }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

