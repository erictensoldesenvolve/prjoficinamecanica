    private void cboEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboEstadoActionPerformed
        String estadoSelecionado = cboEstado.getSelectedItem().toString();
        estado.pesquisaId(estadoSelecionado);
        idEstado = estado.getEstaId();
        List<String> cidades = cidadesBr.buscarCidadesPorEstado(idEstado);
        cboCidade.removeAllItems();
        for (String cidade : cidades) {
            cboCidade.addItem(cidade);
        }
    }//GEN-LAST:event_cboEstadoActionPerformed

    private void cboCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboCidadeActionPerformed
        String cidadeSelecionada = (String) cboCidade.getSelectedItem();

        if (cidadeSelecionada != null && !cidadeSelecionada.isEmpty()) {
            cidadesBr.pesquisaId(cidadeSelecionada);
            cidaId = cidadesBr.getCidaId();
        } 
    }//GEN-LAST:event_cboCidadeActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // BOTÃO DE ALTERAR OS DADOS
        String razaoSocial = null;
        String insc_estadual = null;
        String insc_municipal = null;
        if(txtCnpj.getText().length() > 14){
         razaoSocial = txtRazao.getText().toUpperCase();
         insc_estadual = txtInscEstadual.getText();
         insc_municipal = txtInscMunicipal.getText();
        }
        
        if(usuario.alterar(
                txtEndereco.getText().toUpperCase(),
                Integer.parseInt(txtNumero.getText().trim()),
                txtBairro.getText().toUpperCase(),
                txtCep.getText(),
                idEstado, 
                cidaId, 
                txtEmail.getText().toUpperCase(),
                txtUsuNome.getText().toUpperCase(),
                razaoSocial,
                txtCnpj.getText(),
                insc_estadual, 
                txtTelefone.getText(), 
                insc_municipal,
                txtCompl.getText().toUpperCase(),
                Integer.parseInt(txtUsuId.getText().trim()))
                ){
        JOptionPane.showMessageDialog(null, "Usuário alterado com sucesso!");
    }
    }//GEN-LAST:event_jButton1ActionPerformed


