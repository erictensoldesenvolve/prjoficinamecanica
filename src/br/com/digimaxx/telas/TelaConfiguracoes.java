    private void preencheCampos(){
        usuario.buscar(Principal.usuCnpj);
        txtUsuId.setText(String.valueOf(usuario.getUsuId()));
        txtUsuNome.setText(usuario.getUsuNome());
        txtRazao.setText(usuario.getUsuRazao());
        txtCnpj.setText(usuario.getUsuCnpj());
        txtInscEstadual.setText(usuario.getUsuInsc_estadual());
        txtInscMunicipal.setText(usuario.getUsuInsc_municipal());
        jDataInicio.setDate(usuario.getUsuDataInicio());
        txtEndereco.setText(usuario.getUsuEndereco());
        txtNumero.setText(String.valueOf(usuario.getUsuNumero()));
        txtCompl.setText(usuario.getUsuCompl());
        txtBairro.setText(usuario.getUsuBairro());
        txtCep.setText(usuario.getUsuCep());
        
        estado.pesquisaEstado(usuario.getUsuEstaId());        
        cboEstado.setSelectedItem(estado.getEstaNome().toUpperCase());        
        idEstado = usuario.getUsuEstaId();

        cidadesBr.pesquisaCidade(usuario.getUsuCidaId());
        cboCidade.setSelectedItem(cidadesBr.getCidaNome().toUpperCase());
        cidaId = usuario.getUsuCidaId();
        
        txtEmail.setText(usuario.getUsuEmail());
        txtTelefone.setText(usuario.getUsuTelefone());
        txtLicenca.setText(usuario.getUsuLicenca());
        jDataLicenca.setDate(usuario.getUsuDataLicenca());
    }
    
    
    

